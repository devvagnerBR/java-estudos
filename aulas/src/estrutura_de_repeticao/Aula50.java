// Aula - 50 - Teste de mesa com estrutura repetitiva for
package estrutura_de_repeticao;

public class Aula50 {

    public static void main(String[] args) {

        int X = 3;
        int Y = 0;

        for (int i = 0; i < X; i++) {
            System.out.print(1 + ",");
            Y += 5;
            System.out.println(Y);
        }
    }

}
