package memoria_arrays_listas.vetorII.entities;

public class Product {

    private String name;
    private double price;

    public Product(String name, double price) {
        this.name = name;
        this.price = price;
    }

    // getters and setters

    public String getName() {
        return name;
    }

    public double getPrice() {
        return price;
    }

}
